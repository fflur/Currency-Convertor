package org.database;

public class Curr {
    public String country;
    public String name;
    public String code;
    public int number;

    public Curr(
        String country,
        String name,
        String code,
        int number
    ) {
        this.country = country;
        this.name = name;
        this.code = code;
        this.number = number;
    }
}
